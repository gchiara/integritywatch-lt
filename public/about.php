              <h3>TILS rekomendacijos parlamentarams</h3>
              <ul>
              <li>Susitikimus viešinti savo darbotvarkėse lrs.lt puslapyje arba pateikiant nuorodas į asmenines svetaines;</li>
              <li>Viešinti visus sutikimus su (1) interesų grupėmis (asociacijomis, konfederacijomis, nevyriausybinėmis organizacijomis, paramos ir labdaros fondais ir kt.), (2) registruotais lobistais ir (3) susitikimus, kurių metu gaunami pasiūlymai, susiję su norminių teisės aktų priėmimu, pakeitimu ar panaikinimu.</li>
              <li>Darbotvarkėse nurodyti, su kokios organizacijos atstovais, kur ir kada  susitinkama, kokios jų pareigos,koks susitikimo tikslas ar tema bei aiškiai įvardinti, kokie klausimai aptariami susitikimo metu.</li>
