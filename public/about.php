              <div class="panel-body">
              <p>Svetainėje „ManoSeimas.lt“ galite greitai ir paprastai sužinoti, kaip šios kadencijos Seimo nariai viešina savo darbotvarkes, su kokiomis interesų grupėmis susitinka, lyginti parlamentarų aktyvumą bei frakcijų susitikimų skaičiaus kaitą skirtingų sesijų metu.</p>
              <p>„ManoSeimas.lt“ skelbiama informacija apie XII Seimo kadencijos parlamentarų deklaruotus susitikimus su interesų grupėmis ir registruotais lobistais nuo 2017 m. kovo mėn. 10 d. iki  2020 m. rugpjūčio mėn. 16 d.</p>
              <p>Visi Seimo narių susitikimai (posėdžiai, vidiniai susitikimai, renginiai ir pan.) skelbiami ir reguliariai atnaujinami remiantis lrs.lt atvirais duomenimis. Susitikimų skaičius su interesų grupių atstovais (verslininkais, NVO atstovais, registruotais lobistais, profsąjungomis) nurodytas peržiūrint susitikimus, paskelbtus viešose Seimo narių darbotvarkėse, asmeninėse svetainėse ir svetainėje <a href="https://manosusitikimai.lt/" target="_blank">www.manosusitikimai.lt</a>.</p>
              <h3>Naudojamos sąvokos</h3>
              <p><strong>Interesų grupė</strong> - tai gyventojų grupė ir/ arba juridiniai asmenys, kurie siekia daryti įtaką priimant politinius ir/ arba administracinius sprendimus.</p>
              <p><strong>Susitikimas su interesų grupe</strong> - susitikimas, kurio metu galimai buvo siekiama dalyvauti priimant sprendimus.</p>
              <p>Kadangi darbotvarkėse Seimo nariai vis dar retai aiškiai nurodo susitikimo tikslą, TILS darė prielaidą, kad <u>susitikimai su interesų grupėmis</u> yra susitikimai su verslininkais, lobistais, verslo įmonėmis, verslo asociacijomis, religinių bendruomenių atstovais, nevyriausybinėmis ir profesinėmis organizacijomis, asociacijomis (įskaitant paramos ir labdaros fondus), akademikais ir pan.</p>
              <p>Susitikimais su interesų grupėmis <u>nelaikomi</u> tokie susitikimai, kaip, pavyzdžiui:</p>
              <ul>
              <li>su valstybės tarnautojais, diplomatais, užsienio delegacijų svečiais bei kitais politikais;</li>
              <li>su rinkėjais arba kai politikai dalyvauja renginiuose, parodų atidarymuose, knygų pristatymuose, konferencijose ir pan.;</li>
              <li>pernelyg plačiai ir abstrakčiai įvardinti susitikimai, kai, pavyzdžiui, nurodomas tik asmens vardas arba tik sritis, kurioje veikia interesų grupė („susitikimas su antstoliais, sportininkais“ ir pan.);</li>
              <li>susitikimai, viešinti asmeninėse socialinių tinklų paskyrose.</li>
              </ul>
